// pop => Esse método tirao último elemento do array
// Não precisamos atribuir ele a uma variável

lista = ['Anderson', "Rafaela", "João"]

console.log(lista) // [ 'Anderson', 'Rafaela', 'João' ] > Array antes do pop

lista.pop()

console.log(lista) // [ 'Anderson', 'Rafaela'] > Array depois do pop

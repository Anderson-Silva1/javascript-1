let count = 0

// Essa é a estrutura básica do While
while(count < 10){ // Enquanto "count" for "menor" que 10, ele vai execultar o bloco de código
    console.log(count) // Ele vai imprimir o "count"
    count++ // Ele vai tambem incrementer o número 1 a variável "count"... Se tirarmos isso, o while entrará no Looping Infinito
}


// Parâmetros servem para usarmos os dados que serão enviados a função, dentro da função...
function sum(a,b /* Criamos 2 parâmetros: "a" e "b"... Mas poderia ser qualquer coisa, desde que esteja separado por ","(vírgula) */){
    let sum = a + b // Criamos uma variável para receber os parâmetros "a" e "b" e somá-los...
    console.log(sum)
}

sum(3,2) // Aqui estamos chamando nossa Função e colocando o número "3" no parâmetro "a" e o número "2" no parâmetro "b"... O que, sendo aplicada a função "sum", vai somar "2" e "3" dando o resultado "5"


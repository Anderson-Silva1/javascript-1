// += -> Adição
{
    let num1 = 10

    num1 = num1 + 1 // Dessa forma está correta, mas não é eficiente

    num1 += 1 // Aqui chegamos no mesmo resultado da anterior, e é mais prático
}

// -= -> Subtração
{
    let num1 = 10

    num1 = num1 - 1 // Dessa forma está correta, mas não é eficiente

    num1 -= 1 // Aqui chegamos no mesmo resultado da anterior, e é mais prático
}

// /= -> Divisão
{
    let num1 = 10

    num1 = num1 / 1 // Dessa forma está correta, mas não é eficiente

    num1 /= 1 // Aqui chegamos no mesmo resultado da anterior, e é mais prático
}

// *= -> Multiplicação
{
    let num1 = 10

    num1 = num1 * 1 // Dessa forma está correta, mas não é eficiente

    num1 *= 1 // Aqui chegamos no mesmo resultado da anterior, e é mais prático
}

// %= -> Resto
{
    let num1 = 10

    num1 = num1 % 1 // Dessa forma está correta, mas não é eficiente

    num1 %= 1 // Aqui chegamos no mesmo resultado da anterior, e é mais prático
}
{
    //   Exemplo While

    let i = 0 // Declaramos uma variável com valor 0 a ela
    while(i <= 20) { // Determinamos que enquanto "i" for Menor ou Igual a 20, ele vai repetir e execultar o comando a baixo
        console.log(i) // Aqui ele vai printar na tela o valor de "i"
        i+= 2 // Aqui ele vai somar "+2" a "i", ou seja, ele vai contar de "2" em "2"
    }
}